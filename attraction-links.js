(function toGoogleSearchLinks(){
  try {
    const map = window.attractionLinks || {};
    Object.keys(map).forEach((name) => {
      map[name] = 'https://www.google.com/search?q=' + encodeURIComponent(name);
    });
  } catch {}
})();
